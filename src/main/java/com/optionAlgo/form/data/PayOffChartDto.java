package com.optionAlgo.form.data;

public class PayOffChartDto {
	
	Double changePercent;
	Double currentPnL;
	Double finalPnL;
	Double stockPrice;
	public Double getChangePercent() {
		return changePercent;
	}
	public void setChangePercent(Double changePercent) {
		this.changePercent = changePercent;
	}
	public Double getCurrentPnL() {
		return currentPnL;
	}
	public void setCurrentPnL(Double currentPnL) {
		this.currentPnL = currentPnL;
	}
	public Double getFinalPnL() {
		return finalPnL;
	}
	public void setFinalPnL(Double finalPnL) {
		this.finalPnL = finalPnL;
	}
	public Double getStockPrice() {
		return stockPrice;
	}
	public void setStockPrice(Double stockPrice) {
		this.stockPrice = stockPrice;
	}
	
	

}
